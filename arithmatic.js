let x = 3;
let y = 2;

//different expressions 
console.log(x + y); 
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y); // remainder operator
console.log(x ** y); // exponentiation operator (x to the power of y)

// Increment
console.log(x++);
console.log(++x);

// decrement;
console.log(y--);
console.log(--y);
